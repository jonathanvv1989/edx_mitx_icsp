# Exercise: square
def square(num):
  """
  num: int or float
  returns the square of num
  """
  return num**2

# Exercise: eval quadratic
def evalQuadratic(a, b, c, x):
    """
    a, b, c: int or float constants
    x: int or float, x variable of quadratic equation
    returns value of quadratic equation
    """
    return (a * x**2) + (b * x) + c
